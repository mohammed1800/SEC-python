# A sequence is an ordered collection of items. It can be a list, tuple, string, or any other iterable
# object in Python. For the sake of this example, let's use a list as our sequence.

# Define a list as our sequence
my_list = [10, 20, 30, 40, 50]

# Loop over the elements of the list using a for loop
for item in my_list:
    print(item)
